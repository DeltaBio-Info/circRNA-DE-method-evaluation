#!/bin/python

# =============================================================================
# STAR Alignment Pipeline for Circtools2 Pre-processing
# =============================================================================
# Purpose:
#     Runs STAR alignment on paired-end cleaned FASTQ files for each sample.
#     For every sample, the script performs a paired-end alignment with sorted
#     BAM output and separate mate1/mate2 alignments for downstream chimeric
#     junction processing. The paired-end BAM is indexed with samtools when it
#     is successfully generated.
#
# Inputs:
#     genome_path          :            STAR genome index directory.
#     gtf                  :            Reference genome GTF annotation file.
#     input_folder         :            Directory containing paired-end FASTQ
#                                       files, sorted as alternating R1/R2 files.
#     destination_folder   :            Directory where STAR sample output
#                                       folders will be written.
#     threads              :            Number of CPU threads to use.
#
# Outputs:
#     destination_folder/sample/                    Paired-end STAR output folder.
#     destination_folder/sample/*Aligned.sortedByCoord.out.bam
#                                                   Sorted paired-end BAM file.
#     destination_folder/sample/*Aligned.sortedByCoord.out.bam.bai
#                                                   BAM index generated by samtools.
#     destination_folder/sample/mate1/              STAR output from mate1-only reads.
#     destination_folder/sample/mate2/              STAR output from mate2-only reads.
# =============================================================================

import os
import sys
import subprocess

# =============================================================================
# Command-line inputs and sample discovery
# =============================================================================

genome_path = sys.argv[1]
gtf = sys.argv[2]
file_list = os.listdir(sys.argv[3])
file_list.sort()
destination_folder = sys.argv[4]
threads = sys.argv[5]

i = 0

# =============================================================================
# Process paired FASTQ files
# =============================================================================
# The input folder is expected to contain sorted paired-end FASTQ files. The
# loop advances by two files at a time, uses the R1 filename to derive the R2
# filename, and names each sample from the first underscore-delimited field.
# =============================================================================

for i in range(0,len(file_list),2):
	fastq_path1 = os.path.join(sys.argv[3], file_list[i])
	fastq_path2 = fastq_path1.replace('_R1_', '_R2_')
	name = file_list[i][:-9]
	name = name.split("_")[0]
	
	output_folder = os.path.join(destination_folder, name)
	mate1_folder = os.path.join(output_folder, 'mate1')
	mate2_folder = os.path.join(output_folder, 'mate2')

	if not os.path.exists(output_folder):
		os.makedirs(output_folder, exist_ok=True)

		# Paired-end STAR alignment. This creates the coordinate-sorted BAM,
		# chimeric junction files, gene-count output, and unmapped reads.
		cmd_merged = ['STAR', '--genomeLoad', 'NoSharedMemory','--runThreadN', str(threads),
		'--genomeDir', genome_path,
		'--sjdbGTFfile', gtf,
		'--readFilesIn', fastq_path1, fastq_path2,
		'--readFilesCommand', 'zcat',
		'--outFileNamePrefix', os.path.join(output_folder, name),
		
		'--outReadsUnmapped', 'fastx',
		'--outSAMattributes', 'NH', 'HI', 'AS', 'nM', 'NM', 'MD', 'jM', 'jI', 'XS',
		'--outSJfilterOverhangMin', '15', '15', '15', '15',
		'--outFilterMultimapNmax', '20',
		'--outFilterScoreMin', '1',
		'--outFilterMatchNminOverLread', '0.7',
		'--outFilterMismatchNmax', '999',
		'--outFilterMismatchNoverLmax', '0.05',
		
		'--alignIntronMin', '20',
		'--alignIntronMax', '1000000',
		'--alignMatesGapMax', '1000000',
		'--alignSJoverhangMin', '15',
		'--alignSJDBoverhangMin', '10',
		'--alignSoftClipAtReferenceEnds', 'No',
		
		'--chimSegmentMin', '15',
		'--chimScoreMin', '15',
		'--chimScoreSeparation', '10',
		'--chimJunctionOverhangMin', '15',
		
		'--quantMode', 'GeneCounts',
		'--twopassMode', 'Basic',

		'--outSAMtype', 'BAM', 'SortedByCoordinate',
		'--chimOutType', 'Junctions', 'SeparateSAMold'
		]

		# Mate1-only STAR alignment. Circtools2 uses single-mate chimeric
		# junction evidence in addition to the paired-end alignment.
		cmd_m1 = ['STAR', '--genomeLoad', 'NoSharedMemory','--runThreadN', str(threads),
		'--genomeDir', genome_path,
		'--sjdbGTFfile', gtf,
		'--readFilesIn', fastq_path1,
		'--readFilesCommand', 'zcat',
		'--outFileNamePrefix', os.path.join(mate1_folder, name),
		
		'--outReadsUnmapped', 'fastx',
		'--outSAMattributes', 'NH', 'HI', 'AS', 'nM', 'NM', 'MD', 'jM', 'jI', 'XS',
		'--outSJfilterOverhangMin', '15', '15', '15', '15',
		'--outFilterMultimapNmax', '20',
		'--outFilterScoreMin', '1',
		'--outFilterMatchNminOverLread', '0.7',
		'--outFilterMismatchNmax', '999',
		'--outFilterMismatchNoverLmax', '0.05',
		
		'--alignIntronMin', '20',
		'--alignIntronMax', '1000000',
		'--alignMatesGapMax', '1000000',
		'--alignSJoverhangMin', '15',
		'--alignSJDBoverhangMin', '10',
		'--alignSoftClipAtReferenceEnds', 'No',
		
		'--chimSegmentMin', '15',
		'--chimScoreMin', '15',
		'--chimScoreSeparation', '10',
		'--chimJunctionOverhangMin', '15',
		
		'--quantMode', 'GeneCounts',
		'--twopassMode', 'Basic',
		'--outSAMtype', 'None',
		'--chimOutType', 'Junctions', 'SeparateSAMold'		
		]

		# Mate2-only STAR alignment with the same chimeric-junction settings as
		# the mate1-only run.
		cmd_m2 = ['STAR', '--genomeLoad', 'NoSharedMemory','--runThreadN', str(threads),
		'--genomeDir', genome_path,
		'--sjdbGTFfile', gtf,
		'--readFilesIn', fastq_path2,
		'--readFilesCommand', 'zcat',
		'--outFileNamePrefix', os.path.join(mate2_folder, name),
		
		'--outReadsUnmapped', 'fastx',
		'--outSAMattributes', 'NH', 'HI', 'AS', 'nM', 'NM', 'MD', 'jM', 'jI', 'XS',
		'--outSJfilterOverhangMin', '15', '15', '15', '15',
		'--outFilterMultimapNmax', '20',
		'--outFilterScoreMin', '1',
		'--outFilterMatchNminOverLread', '0.7',
		'--outFilterMismatchNmax', '999',
		'--outFilterMismatchNoverLmax', '0.05',
		
		'--alignIntronMin', '20',
		'--alignIntronMax', '1000000',
		'--alignMatesGapMax', '1000000',
		'--alignSJoverhangMin', '15',
		'--alignSJDBoverhangMin', '10',
		'--alignSoftClipAtReferenceEnds', 'No',
		
		'--chimSegmentMin', '15',
		'--chimScoreMin', '15',
		'--chimScoreSeparation', '10',
		'--chimJunctionOverhangMin', '15',
		
		'--quantMode', 'GeneCounts',
		'--twopassMode', 'Basic',
		'--outSAMtype', 'None',
		'--chimOutType', 'Junctions', 'SeparateSAMold'		
		]

		print(f'[INFO] Running PE alignment for {name}...')
		result = subprocess.run(cmd_merged, check=True)
		bam_file = os.path.join(output_folder, name + "Aligned.sortedByCoord.out.bam")

		# Index the paired-end BAM only if STAR produced the expected file.
		if os.path.exists(bam_file):
			print(f"[INFO] Indexing BAM for {name}...")
			subprocess.run([
				'samtools', 'index',
				'-@', str(threads),
				bam_file
			], check=True)
		else:
			print(f"[WARNING] BAM file not found for {name}, skipping indexing.")
			
		if not os.path.exists(mate1_folder):
			os.makedirs(mate1_folder, exist_ok=True)
			print(f"[INFO] Running {name} mate1 alignment...")
			m1_res = subprocess.run(cmd_m1, check=True)
		if not os.path.exists(mate2_folder):
			os.makedirs(mate2_folder, exist_ok=True)
			print(f"[INFO] Running {name} mate2 alignment")
			m2_res = subprocess.run(cmd_m2, check=True)
	else:
		print(f"[INFO] Sample: {name} already aligned!")

